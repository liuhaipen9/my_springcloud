/**
 * @author 刘海鹏
 * @iphone 13714872954
 * @date 2019/5/1下午7:55
 */

@EnableDiscoveryClient
@EnableFeignClients
@SpringBootApplication
public class EurekaConsumerFeignApplication {

    public static void main(String[] args) {
        SpringApplication.run(EurekaConsumerFeignApplication.class, args);
    }

}
